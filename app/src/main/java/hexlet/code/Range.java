package hexlet.code;

public class Range {
    private int numFrom;
    private int numTo;

    public Range(int numFrom, int numTo) {
        this.numFrom = numFrom;
        this.numTo = numTo;
    }

    public int getNumFrom() {
        return numFrom;
    }

    public int getNumTo() {
        return numTo;
    }
}
